#!/usr/bin/env node
/**
 * scripts/validate-dossiers.mjs
 * ----------------------------------------------------------------
 * Walks every dossier JSON in src/data/dossiers/ and validates:
 *
 *   1.  Required top-level fields are present and well-typed.
 *   2.  Section type discriminator (`t`) is in the known set.
 *   3.  Each section's required fields per its type.
 *   4.  Every figure referenced by a section exists in the
 *       dossier.figures[] array.
 *   5.  Every figure file at figure.src exists on disk in /public.
 *   6.  Citations are present on every fact/case/diagram section
 *       that asserts a contested empirical claim (heuristic: body
 *       contains a number, year, percentage, or proper-noun source).
 *   7.  No banned terms (stealth check) — AI references, partisan
 *       slurs, profanity in user-facing copy.
 *   8.  Word count is plausible — section bodies must sum to within
 *       ±15% of dossier.wordCount.
 *   9.  Slug is URL-safe and matches id case-insensitively.
 *  10.  All section ids are unique.
 *
 * Exit code 0 on clean pass, 1 on any error. Warnings do not fail
 * the build but are surfaced to stdout.
 *
 * Integration: add to the build chain in package.json AFTER
 *   `validate-issues` and BEFORE `validate-language`. Example:
 *
 *     "build": "... && node scripts/validate-issues.mjs && node scripts/validate-dossiers.mjs && node scripts/validate-language.mjs && astro build"
 *
 * No external dependencies — uses only Node stdlib.
 */

import { readdirSync, readFileSync, existsSync, statSync } from 'node:fs';
import { join, resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, '..');
const DOSSIERS_DIR = join(ROOT, 'src', 'data', 'dossiers');
const PUBLIC_DIR = join(ROOT, 'public');

const SECTION_TYPES = new Set([
  'cover', 'tldr', 'chapter', 'concept', 'fact', 'timeline',
  'case', 'reframe', 'diagram', 'quote', 'mistake', 'view',
  'further_reading', 'cite_as',
]);

const LENSES = new Set([
  'Political', 'Economic', 'Legal', 'Regional', 'Social', 'Constitutional',
]);

// Stealth check — never let these surface in user-facing copy.
const BANNED_PATTERNS = [
  /\bAI[- ]generated\b/i,
  /\bpowered by\b/i,
  /\bGPT[- ]?[0-9]/i,
  /\bClaude\s+\d/i,
  /\bChatGPT\b/i,
  /\bGemini\s+(Pro|Ultra|Flash|2\.\d|1\.\d)/i,
  /\bopen[- ]?AI\b/i,
  /\bgenerated by\s+(an?\s+)?(AI|LLM|model|machine)/i,
];

// Banned for editorial register (partisan language, sloppy phrasings).
const REGISTER_WARNINGS = [
  /\bobviously\b/i,
  /\bclearly[,\s]/i,
  /\bof course[,\s]/i,
  /\bin fact[,\s]/i,
  /\bmoreover\b/i,
  /\bfurthermore\b/i,
];

let errors = [];
let warnings = [];

function err(msg) { errors.push(msg); }
function warn(msg) { warnings.push(msg); }

function ok(label) { console.log(`  ✓ ${label}`); }

function validateDossier(path) {
  console.log(`\n› ${path.replace(ROOT + '/', '')}`);
  let raw, doc;
  try {
    raw = readFileSync(path, 'utf8');
    doc = JSON.parse(raw);
  } catch (e) {
    err(`Could not parse JSON: ${e.message}`);
    return;
  }

  /* ---------------- 1. top-level required fields ---------------- */
  const REQUIRED = [
    'id', 'format', 'series', 'title', 'subtitle', 'slug',
    'estReadMinutes', 'wordCount', 'published', 'sourceDate',
    'edition', 'primaryLens', 'sections', 'figures', 'designTokens',
    'citeAs', 'ogImage', 'accentColor',
  ];
  for (const k of REQUIRED) {
    if (!(k in doc)) err(`Missing top-level field: ${k}`);
  }
  if (doc.format !== 'dossier') err(`format must equal "dossier" (got "${doc.format}")`);
  if (!LENSES.has(doc.primaryLens)) err(`Unknown primaryLens: ${doc.primaryLens}`);
  ok(`top-level fields`);

  /* ---------------- 9. slug + id ---------------- */
  if (doc.slug && !/^[a-z0-9-]+$/.test(doc.slug)) {
    err(`slug must be URL-safe (lowercase, digits, hyphens): "${doc.slug}"`);
  }
  if (doc.id && !/^D\d{3,}$/.test(doc.id)) {
    err(`id must match /^D\\d{3,}$/ (got "${doc.id}")`);
  }
  ok(`slug and id format`);

  /* ---------------- 2 + 3. sections by type ---------------- */
  if (!Array.isArray(doc.sections) || doc.sections.length === 0) {
    err(`sections must be a non-empty array`);
    return;
  }

  const sectionIds = new Set();
  let bodyChars = 0;

  for (const [i, s] of doc.sections.entries()) {
    const at = `sections[${i}] (${s.id ?? '?'})`;
    if (!s.id) err(`${at}: missing id`);
    if (!s.t) { err(`${at}: missing t`); continue; }
    if (!SECTION_TYPES.has(s.t)) err(`${at}: unknown t "${s.t}"`);
    if (sectionIds.has(s.id)) err(`${at}: duplicate section id`);
    sectionIds.add(s.id);

    switch (s.t) {
      case 'cover':
        ['title', 'subtitle', 'seriesMark', 'estReadMinutes', 'whatYoullLearn', 'heroFigure'].forEach(k => {
          if (!(k in s)) err(`${at}: cover section missing ${k}`);
        });
        if (!Array.isArray(s.whatYoullLearn) || s.whatYoullLearn.length < 3) {
          warn(`${at}: cover.whatYoullLearn should have ≥3 bullets`);
        }
        break;
      case 'tldr':
        if (!Array.isArray(s.bullets) || s.bullets.length < 3) {
          err(`${at}: tldr must have ≥3 bullets`);
        }
        s.bullets?.forEach((b, j) => { bodyChars += String(b).length; });
        break;
      case 'chapter':
        if (typeof s.n !== 'number') err(`${at}: chapter.n must be a number`);
        if (!s.title || !s.subtitle) err(`${at}: chapter needs title and subtitle`);
        break;
      case 'concept':
      case 'case':
        if (!s.title) err(`${at}: ${s.t} needs title`);
        if (!s.body) err(`${at}: ${s.t} needs body`);
        bodyChars += String(s.body || '').length;
        break;
      case 'fact':
        if (!s.title) err(`${at}: fact needs title`);
        if (!s.body) err(`${at}: fact needs body`);
        if (!s.lens) err(`${at}: fact needs lens`);
        if (s.lens && !LENSES.has(s.lens)) err(`${at}: unknown lens "${s.lens}"`);
        if (hasEmpiricalSignals(s.body) && (!s.citations || s.citations.length === 0)) {
          err(`${at}: fact section asserts numbers/dates/named sources but has no citations[]`);
        }
        bodyChars += String(s.body || '').length;
        break;
      case 'timeline':
        if (!Array.isArray(s.events) || s.events.length === 0) {
          err(`${at}: timeline.events must be a non-empty array`);
        }
        bodyChars += String(s.intro || '').length;
        s.events?.forEach((ev, j) => {
          if (!ev.year || !ev.label) err(`${at}: timeline event ${j} missing year/label`);
          bodyChars += String(ev.outcome || '').length;
        });
        break;
      case 'reframe':
      case 'view':
        if (!s.body) err(`${at}: ${s.t} needs body`);
        bodyChars += String(s.body || '').length;
        break;
      case 'diagram':
        if (!s.figure) err(`${at}: diagram needs a figure ref`);
        if (!s.body) err(`${at}: diagram needs body`);
        bodyChars += String(s.body || '').length;
        break;
      case 'quote':
        if (!s.source) err(`${at}: quote needs source`);
        if (!s.body) err(`${at}: quote needs body`);
        bodyChars += String(s.body || '').length;
        bodyChars += String(s.commentary || '').length;
        // editorNote is optional but if present must be string
        if (s.editorNote != null && typeof s.editorNote !== 'string') {
          err(`${at}: quote.editorNote must be a string when present`);
        }
        break;
      case 'mistake':
        if (!s.claim) err(`${at}: mistake needs claim`);
        if (!s.correction) err(`${at}: mistake needs correction`);
        bodyChars += String(s.correction || '').length;
        break;
      case 'further_reading':
        if (!Array.isArray(s.items) || s.items.length === 0) {
          err(`${at}: further_reading.items must be non-empty`);
        }
        break;
      case 'cite_as':
        if (!s.format) err(`${at}: cite_as needs format`);
        if (!s.license) err(`${at}: cite_as needs license`);
        break;
    }

    /* ---------- 7. banned terms (whole-section sweep) ---------- */
    const allText = JSON.stringify(s);
    for (const re of BANNED_PATTERNS) {
      if (re.test(allText)) {
        err(`${at}: banned term matched ${re}`);
      }
    }
    for (const re of REGISTER_WARNINGS) {
      if (re.test(allText)) {
        warn(`${at}: register flag — soft word matched ${re} (consider rewording)`);
      }
    }
  }
  ok(`${doc.sections.length} sections, all typed`);

  /* ---------------- 4 + 5. figures: references and files ---------------- */
  if (!Array.isArray(doc.figures)) {
    err(`figures must be an array`);
  } else {
    const figIds = new Set(doc.figures.map(f => f.id));
    for (const s of doc.sections) {
      if (s.figure && !figIds.has(s.figure)) {
        err(`section ${s.id}: references missing figure "${s.figure}"`);
      }
      if (s.heroFigure && !figIds.has(s.heroFigure)) {
        err(`section ${s.id}: references missing heroFigure "${s.heroFigure}"`);
      }
    }
    for (const f of doc.figures) {
      if (!f.id) err(`figure missing id`);
      if (!f.alt) err(`figure ${f.id}: missing alt text (required for a11y)`);
      if (!f.src) err(`figure ${f.id}: missing src`);
      // Only check files for external assets (PNG/JPG/SVG on disk),
      // not for code-rendered figures (handled by Svelte components).
      const isExternalAsset = /\.(png|jpe?g|gif|webp)$/i.test(f.src || '');
      if (isExternalAsset && f.src) {
        const filePath = join(PUBLIC_DIR, f.src.replace(/^\//, ''));
        if (!existsSync(filePath)) {
          err(`figure ${f.id}: file not found at public${f.src}`);
        } else {
          const stat = statSync(filePath);
          if (stat.size < 200) {
            warn(`figure ${f.id}: file is suspiciously small (${stat.size} bytes) — placeholder?`);
          }
        }
      }
    }
    ok(`${doc.figures.length} figures, references resolved`);
  }

  /* ---------------- 6. citation density ---------------- */
  // Soft check — surfaces as warning, not error. The per-section
  // check above catches the hard cases.
  let factSections = doc.sections.filter(s => s.t === 'fact').length;
  let factsWithCitations = doc.sections.filter(
    s => s.t === 'fact' && Array.isArray(s.citations) && s.citations.length
  ).length;
  if (factSections > 0 && factsWithCitations / factSections < 0.8) {
    warn(`citation density: only ${factsWithCitations}/${factSections} fact sections have citations[] (target ≥80%)`);
  }
  ok(`citation density: ${factsWithCitations}/${factSections} fact sections cite sources`);

  /* ---------------- 8. word count plausibility ---------------- */
  // Rough conversion: ~5 chars per word, English. ±15% tolerance.
  const estimatedWords = Math.round(bodyChars / 5);
  const declared = doc.wordCount ?? 0;
  if (declared > 0) {
    const ratio = estimatedWords / declared;
    if (ratio < 0.7 || ratio > 1.3) {
      warn(
        `word count: declared ${declared}, estimated ${estimatedWords} from body chars ` +
        `(ratio ${ratio.toFixed(2)} — outside 0.7–1.3 tolerance). Update wordCount or check for missing sections.`
      );
    } else {
      ok(`word count: declared ${declared}, estimated ${estimatedWords} (ratio ${ratio.toFixed(2)})`);
    }
  }
}

function hasEmpiricalSignals(text) {
  if (!text) return false;
  return (
    /\b\d{1,4}([,.]\d+)?(\s*(per cent|%|bn|billion|million|m|seats|years?))?\b/.test(text) ||
    /\b(19|20)\d{2}\b/.test(text) ||
    /\b(Article|Section|Act|Constitution|Ordinance|Bill)\b/.test(text) ||
    /\bRM\d/.test(text)
  );
}

/* ---------------------------- run ---------------------------- */

function main() {
  console.log('Validating dossiers …');

  let files;
  try {
    files = readdirSync(DOSSIERS_DIR).filter(f => f.endsWith('.json'));
  } catch (e) {
    console.error(`Cannot read ${DOSSIERS_DIR}: ${e.message}`);
    process.exit(1);
  }

  if (files.length === 0) {
    console.log('No dossiers to validate.');
    process.exit(0);
  }

  for (const f of files.sort()) {
    validateDossier(join(DOSSIERS_DIR, f));
  }

  console.log('');
  if (warnings.length) {
    console.log(`Warnings (${warnings.length}):`);
    warnings.forEach(w => console.log(`  ⚠  ${w}`));
  }
  if (errors.length) {
    console.log(`\nErrors (${errors.length}):`);
    errors.forEach(e => console.log(`  ✗  ${e}`));
    process.exit(1);
  }
  console.log(`✓ ${files.length} dossier${files.length === 1 ? '' : 's'} validated — no errors.`);
}

main();
